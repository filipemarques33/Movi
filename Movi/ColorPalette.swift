import UIKit

extension UIColor {
    
    @nonobjc class var lightNavyBlue: UIColor {
        return UIColor(red: 46.0 / 255.0, green: 45.0 / 255.0, blue: 133.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var dodgerBlue: UIColor {
        return UIColor(red: 70.0 / 255.0, green: 138.0 / 255.0, blue: 242.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var paleGrey: UIColor {
        return UIColor(red: 244.0 / 255.0, green: 244.0 / 255.0, blue: 246.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var cloudyBlue: UIColor {
        return UIColor(red: 188.0 / 255.0, green: 187.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }
    
}
